#!/usr/bin/env node
// generate-error-docs: generates docs/error-codes.md plus the shared registries
// at wrapper/error-codes.ts, cli/src/error-codes.ts, and mcp/src/error-codes.ts
// from docs/error-codes.json (the canonical error-registry).
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const args = new Set(process.argv.slice(2));
const registryPath = path.join(root, "docs", "error-codes.json");
const markdownPath = path.join(root, "docs", "error-codes.md");
const tsTargets = [
    path.join(root, "wrapper", "error-codes.ts"),
    path.join(root, "cli", "src", "error-codes.ts"),
    path.join(root, "mcp", "src", "error-codes.ts"),
];

const registry = JSON.parse(fs.readFileSync(registryPath, "utf8"));

function cell(value) {
    if (Array.isArray(value)) return value.length ? value.join(", ") : "-";
    return String(value).replaceAll("|", "\\|");
}

function markdownFor(value) {
    const lines = [];
    lines.push("<!-- Generated by scripts/generate-error-docs.mjs. Edit docs/error-codes.json, then run `make error-docs`. -->");
    lines.push("");
    lines.push("# Error Codes and Recovery Contract");
    lines.push("");
    lines.push(value.purpose);
    lines.push("");
    lines.push("## Registry");
    lines.push("");
    lines.push("| Code | Reachable | HTTP status | Retry | Surfaces | Meaning | Recovery |");
    lines.push("|---|---|---|---|---|---|---|");
    for (const code of value.codes) {
        const reachable = code.reachable === false ? "no" : "yes";
        lines.push(`| \`${code.code}\` | ${reachable} | ${cell(code.httpStatus)} | ${code.retry ? "yes" : "no"} | ${cell(code.surfaces)} | ${cell(code.meaning)} | ${cell(code.recovery)} |`);
    }
    lines.push("");
    lines.push("## Adoption rule");
    lines.push("");
    lines.push("Runtime surfaces should import or generate from this registry instead of inventing local wording. Until every runtime is wired to the registry, docs and tests should compare behavior against these codes.");
    lines.push("");
    return `${lines.join("\n")}\n`;
}

function typescriptFor(value) {
    return `/* Generated by scripts/generate-error-docs.mjs. Edit docs/error-codes.json, then run make error-docs. */
export interface ClockifyErrorCodeEntry {
    code: string;
    /** False for documented taxonomy with no SDK-observable runtime signal. */
    reachable?: boolean;
    httpStatus: readonly number[];
    retry: boolean;
    surfaces: readonly string[];
    meaning: string;
    recovery: string;
}

export const CLOCKIFY_ERROR_CODES = ${JSON.stringify(value.codes, null, 4)} as const satisfies readonly ClockifyErrorCodeEntry[];

export type ClockifyErrorCode = (typeof CLOCKIFY_ERROR_CODES)[number]["code"];

export function errorCodeForStatus(status: number | undefined): ClockifyErrorCode | undefined {
    if (status == null) return undefined;
    return CLOCKIFY_ERROR_CODES.find((entry) => (entry.httpStatus as readonly number[]).includes(status))?.code;
}

export function errorCodeForMessage(message: string): ClockifyErrorCode {
    if (/(unauthorized|forbidden|permission|api[\\s_-]?key|addon[\\s_-]?token|CLOCKIFY_API_KEY|CLOCKIFY_ADDON_TOKEN|workspace)/i.test(message)) return "auth_or_permission";
    if (/(required|provide|missing|invalid|unknown option|unknown command|could not parse|not valid|must use|must not|confirmation token)/i.test(message)) {
        return "invalid_request";
    }
    if (/not found/i.test(message)) return "not_found";
    if (/(rate.?limit|too many requests)/i.test(message)) return "rate_limited";
    if (/(network|fetch failed|econnreset|dns|tls)/i.test(message)) return "connection_error";
    if (/(abort|cancel)/i.test(message)) return "aborted";
    return "error";
}

export function errorCodeEntry(code: ClockifyErrorCode): ClockifyErrorCodeEntry {
    return CLOCKIFY_ERROR_CODES.find((entry) => entry.code === code) ?? CLOCKIFY_ERROR_CODES[0];
}

export function recoveryForCode(code: ClockifyErrorCode): string {
    return errorCodeEntry(code).recovery;
}

export function retryableForCode(code: ClockifyErrorCode): boolean {
    return errorCodeEntry(code).retry;
}
`;
}

function validate(value) {
    const seen = new Set();
    for (const item of value.codes) {
        if (!item.code || typeof item.code !== "string") throw new Error("every code needs a string code");
        if (seen.has(item.code)) throw new Error(`duplicate code: ${item.code}`);
        seen.add(item.code);
        if (!Array.isArray(item.httpStatus)) throw new Error(`${item.code}: httpStatus must be an array`);
        if (!Array.isArray(item.surfaces) || item.surfaces.length === 0) throw new Error(`${item.code}: surfaces must be a non-empty array`);
        if (typeof item.retry !== "boolean") throw new Error(`${item.code}: retry must be boolean`);
        if (typeof item.reachable !== "boolean") throw new Error(`${item.code}: reachable must be boolean`);
        if (!item.meaning || !item.recovery) throw new Error(`${item.code}: meaning and recovery are required`);
    }
}

validate(registry);
const expected = markdownFor(registry);
const expectedTs = typescriptFor(registry);

if (args.has("--write")) {
    fs.writeFileSync(markdownPath, expected);
    for (const target of tsTargets) fs.writeFileSync(target, expectedTs);
    console.log("wrote docs/error-codes.md and package error-code modules");
    process.exit(0);
}

if (args.has("--check")) {
    const current = fs.existsSync(markdownPath) ? fs.readFileSync(markdownPath, "utf8") : "";
    const stale = [];
    if (current !== expected) stale.push("docs/error-codes.md");
    for (const target of tsTargets) {
        const currentTs = fs.existsSync(target) ? fs.readFileSync(target, "utf8") : "";
        if (currentTs !== expectedTs) stale.push(path.relative(root, target));
    }
    if (stale.length > 0) {
        console.error(`Error code docs drift: ${stale.join(", ")}. Run make error-docs.`);
        process.exit(1);
    }
    console.log("error code docs are current");
    process.exit(0);
}

process.stdout.write(expected);
