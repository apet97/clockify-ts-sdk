// Official-vs-custom OpenAPI drift: pure report library (no side effects).
//
// Compares Clockify's official OpenAPI surface against this repo's corrected
// snapshot and the live-evidence ledger, then renders the agent/human-friendly
// drift report and the three generated trust surfaces. The executable wrapper is
// scripts/official-openapi-drift.mjs; this module is import-only so the gate, the
// report target, and any tooling share one normalization implementation.
//
// Inputs are read-only: spec/official/clockify.official.openapi.yaml (committed
// upstream snapshot), spec/corrected/clockify.corrected.openapi.yaml (the trusted
// custom spec — never mutated here), and spec/evidence/discrepancies.md.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import YAML from "yaml";

export const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
export const HTTP_METHODS = ["get", "post", "put", "patch", "delete", "head", "options", "trace"];

export const SPEC_PATHS = {
    official: "spec/official/clockify.official.openapi.yaml",
    corrected: "spec/corrected/clockify.corrected.openapi.yaml",
    discrepancies: "spec/evidence/discrepancies.md",
};

export const OFFICIAL_OPENAPI_URL = "https://docs.clockify.me/openapi.json";

// Generated trust surfaces produced from this comparison.
export const GENERATED_DOCS = {
    specDiff: "docs/spec-diff-official.md",
    specConfidence: "docs/spec-confidence.md",
    liveEvidence: "docs/live-evidence-index.md",
};

const GENERATOR_BANNER =
    "<!-- Generated by scripts/official-openapi-drift.mjs. Do not edit by hand; run `make official-openapi-report`. -->";

// --- normalization -----------------------------------------------------------

// Canonicalize a spec path so the official (/v1-prefixed, generic {id}) and
// corrected (no /v1, typed {clientId}) surfaces share one join key. Path params
// collapse to positional `{}` because operationId only matches 39/158 of the
// time and the typed-vs-generic param names diverge by resource.
export function canonicalizePath(rawPath) {
    return String(rawPath)
        .replace(/^\/v1(?=\/)/, "")
        .replace(/\{[^}]+\}/g, "{}");
}

export function operationKey(method, rawPath) {
    return `${method.toUpperCase()} ${canonicalizePath(rawPath)}`;
}

function effectiveHost(op, pathItem, doc) {
    return (
        op?.servers?.[0]?.url ??
        op?.["x-clockify-host"] ??
        pathItem?.servers?.[0]?.url ??
        doc?.servers?.[0]?.url ??
        null
    );
}

function operationSecuritySchemes(op, doc) {
    const security = Array.isArray(op?.security) ? op.security : doc?.security;
    if (!Array.isArray(security)) return [];
    const names = new Set();
    for (const requirement of security) {
        if (requirement && typeof requirement === "object") {
            for (const name of Object.keys(requirement)) names.add(name);
        }
    }
    return [...names].sort();
}

function responseCodes(op) {
    if (!op?.responses || typeof op.responses !== "object") return [];
    return Object.keys(op.responses).sort();
}

// Parse an OpenAPI document object into an operation map keyed by method+path.
export function indexOperations(doc) {
    const map = new Map();
    const paths = doc?.paths && typeof doc.paths === "object" ? doc.paths : {};
    for (const [rawPath, pathItem] of Object.entries(paths)) {
        if (!pathItem || typeof pathItem !== "object") continue;
        for (const method of HTTP_METHODS) {
            const op = pathItem[method];
            if (!op || typeof op !== "object") continue;
            const key = operationKey(method, rawPath);
            map.set(key, {
                key,
                method: method.toUpperCase(),
                rawPath,
                canonicalPath: canonicalizePath(rawPath),
                operationId: op.operationId ?? null,
                tags: Array.isArray(op.tags) ? op.tags : [],
                host: effectiveHost(op, pathItem, doc),
                security: operationSecuritySchemes(op, doc),
                hasRequestBody: Boolean(op.requestBody),
                responseCodes: responseCodes(op),
                liveStatus: op["x-clockify-live-status"] ?? null,
                quarantine: op["x-clockify-source-quarantine"] ?? null,
            });
        }
    }
    return map;
}

export function securitySchemeIndex(doc) {
    const schemes = doc?.components?.securitySchemes;
    if (!schemes || typeof schemes !== "object") return {};
    const out = {};
    for (const [name, scheme] of Object.entries(schemes)) {
        out[name] = {
            type: scheme?.type ?? null,
            in: scheme?.in ?? null,
            header: scheme?.name ? String(scheme.name).toLowerCase() : null,
        };
    }
    return out;
}

// Detect YAML vs JSON by extension/content and parse into a doc object.
export function parseSpec(text, hint = "") {
    const trimmed = text.trimStart();
    if (hint.endsWith(".json") || trimmed.startsWith("{")) return JSON.parse(text);
    return YAML.parse(text);
}

export function loadSpecFile(relativeOrAbsolute) {
    const abs = path.isAbsolute(relativeOrAbsolute)
        ? relativeOrAbsolute
        : path.join(ROOT, relativeOrAbsolute);
    const text = fs.readFileSync(abs, "utf8");
    return parseSpec(text, abs);
}

// --- discrepancy ledger parsing ----------------------------------------------

// Extract per-entry status + phantom/dead-endpoint markers from the evidence
// ledger. Phantom endpoints are written as `slug` (`METHOD /path`) near a
// "No static resource" / "not bound" / 404 note.
export function parseDiscrepancies(markdown) {
    const entries = [];
    const phantomEndpoints = [];
    const sections = markdown.split(/^### /m).slice(1);
    for (const section of sections) {
        const slug = (section.match(/^`([^`]+)`/) || [])[1] ?? (section.match(/^([^\n—]+)/) || [])[1]?.trim();
        const status = (section.match(/\*\*Status:\*\*\s*`?([^`\n|]+)`?/) || [])[1]?.trim() ?? null;
        // Skip the ledger's Template section (slug placeholder like `<short slug ...>`).
        if (!slug || slug.includes("<")) continue;
        entries.push({ slug, status });
    }
    // Phantom / dead-route bullets can live anywhere (inline in the routing entry
    // or in a dedicated quarantine entry). Scan the whole ledger.
    for (const match of markdown.matchAll(/`([\w.]+)`\s*\(`(GET|POST|PUT|PATCH|DELETE)\s+([^`]+)`\)/g)) {
        const window = markdown.slice(match.index, match.index + 500);
        if (/No static resource|phantom|not bound|404/i.test(window)) {
            phantomEndpoints.push({
                slug: match[1],
                method: match[2].toUpperCase(),
                rawPath: match[3],
                canonicalPath: canonicalizePath(match[3]),
            });
        }
    }
    return { entries, phantomEndpoints };
}

// Live-probed status slugs that count as positive live verification.
const LIVE_PROVEN_STATUSES = [
    "live-success",
    "live-verified",
    "fixed-in-canonical-generator",
    "fixed-in-generator-script",
    "fixed-at-source",
    "compensated-in-tool-layer",
    "compensated-in-corrected-spec",
];

export function liveProvenStatuses(parsed) {
    return parsed.entries.filter((e) => e.status && LIVE_PROVEN_STATUSES.some((s) => e.status.includes(s)));
}

// --- diff computation --------------------------------------------------------

export function computeDiff({ official, corrected, officialSchemes, correctedSchemes, phantomEndpoints }) {
    const officialKeys = new Set(official.keys());
    const correctedKeys = new Set(corrected.keys());
    // The evidence ledger records phantom routes workspace-relative
    // (`PUT /projects/{projectId}/archive`) while the corrected spec carries the
    // full `/workspaces/{workspaceId}/...` path, so match on method + path suffix.
    const phantoms = phantomEndpoints ?? [];
    const isPhantom = (op) =>
        phantoms.some(
            (p) =>
                p.method === op.method &&
                (op.canonicalPath === p.canonicalPath || op.canonicalPath.endsWith(p.canonicalPath)),
        );

    const newOfficial = [];
    const customBetter = [];
    const conflicts = [];
    const phantomRisk = [];

    for (const key of officialKeys) {
        if (!correctedKeys.has(key)) newOfficial.push(official.get(key));
    }
    for (const key of correctedKeys) {
        const cor = corrected.get(key);
        if (!officialKeys.has(key)) {
            customBetter.push(cor);
        }
        if (isPhantom(cor)) phantomRisk.push(cor);
    }

    // Shallow wire-shape conflicts on the shared surface: response-code set,
    // request-body presence, and host. Deep field-level shape diffs are tracked
    // in the live-evidence ledger (see live-evidence-index.md), not here.
    for (const key of officialKeys) {
        if (!correctedKeys.has(key)) continue;
        const off = official.get(key);
        const cor = corrected.get(key);
        const reasons = [];
        const offCodes = off.responseCodes.join(",");
        const corCodes = cor.responseCodes.join(",");
        if (offCodes !== corCodes && offCodes && corCodes) {
            reasons.push(`response codes official[${offCodes}] vs custom[${corCodes}]`);
        }
        if (off.hasRequestBody !== cor.hasRequestBody) {
            reasons.push(`requestBody official=${off.hasRequestBody} vs custom=${cor.hasRequestBody}`);
        }
        if (reasons.length > 0) conflicts.push({ key, off, cor, reasons });
    }

    // Auth-scheme drift (scheme-level, not per-op): schemes the official surface
    // declares that the custom surface does not model, and header-casing.
    const authNotes = [];
    for (const [name, scheme] of Object.entries(officialSchemes)) {
        const correctedMatch = Object.entries(correctedSchemes).find(
            ([, c]) => c.header && scheme.header && c.header === scheme.header,
        );
        if (!correctedMatch) {
            authNotes.push(
                `official scheme ${name} (${scheme.in}:${scheme.header}) has no header-equivalent in custom`,
            );
        }
    }

    // Host overrides present only in custom (e.g. reports/audit-log hosts).
    const hostNotes = [];
    const correctedHosts = new Set([...corrected.values()].map((o) => o.host).filter(Boolean));
    for (const host of correctedHosts) {
        const inOfficial = [...official.values()].some((o) => o.host && o.host.includes(hostKeyword(host)));
        if (!inOfficial && /reports|auditlog/i.test(host)) {
            hostNotes.push(`custom routes ${host} (host override present only in custom)`);
        }
    }

    return {
        summary: {
            officialOps: official.size,
            correctedOps: corrected.size,
            common: officialKeys.size - newOfficial.length,
            newOfficial: newOfficial.length,
            customBetter: customBetter.length,
            conflicts: conflicts.length,
            phantomRisk: phantomRisk.length,
        },
        newOfficial: sortOps(newOfficial),
        customBetter: sortOps(customBetter),
        conflicts: conflicts.sort((a, b) => a.key.localeCompare(b.key)),
        phantomRisk: sortOps(phantomRisk),
        authNotes: authNotes.sort(),
        hostNotes: hostNotes.sort(),
    };
}

function hostKeyword(url) {
    const match = url.match(/https?:\/\/([^/]+)/);
    return match ? match[1].split(".")[0] : url;
}

function sortOps(ops) {
    return [...ops].sort((a, b) => a.key.localeCompare(b.key));
}

// --- agent/human-friendly prefixed report ------------------------------------

// Emit the stable-prefix lines (NEW_OFFICIAL_ENDPOINT / CUSTOM_BETTER / CONFLICT
// / PHANTOM_RISK) an agent or human can grep deterministically.
export function renderReportLines(diff) {
    const lines = [];
    for (const op of diff.newOfficial) {
        lines.push(`NEW_OFFICIAL_ENDPOINT: ${op.method} ${stripV1(op.rawPath)}  (operationId=${op.operationId ?? "n/a"})`);
    }
    for (const note of diff.hostNotes) lines.push(`CUSTOM_BETTER: ${note}`);
    for (const note of diff.authNotes) lines.push(`CUSTOM_BETTER: ${note}`);
    for (const op of diff.customBetter) {
        lines.push(`CUSTOM_BETTER: ${op.method} ${op.rawPath} present only in custom (live-status=${op.liveStatus ?? "n/a"})`);
    }
    for (const conflict of diff.conflicts) {
        lines.push(`CONFLICT: ${conflict.key} — ${conflict.reasons.join("; ")}`);
    }
    for (const op of diff.phantomRisk) {
        lines.push(
            `PHANTOM_RISK: ${op.method} ${op.rawPath} in custom but live evidence reports a dead route (404 / not bound)`,
        );
    }
    return lines;
}

function stripV1(rawPath) {
    return String(rawPath).replace(/^\/v1(?=\/)/, "");
}

// --- generated markdown surfaces ---------------------------------------------

function table(headers, rows) {
    const head = `| ${headers.join(" | ")} |`;
    const sep = `| ${headers.map(() => "---").join(" | ")} |`;
    const body = rows.map((r) => `| ${r.join(" | ")} |`).join("\n");
    return `${head}\n${sep}\n${body}`;
}

export function renderSpecDiff(diff, { officialSource } = {}) {
    const s = diff.summary;
    const out = [];
    out.push(GENERATOR_BANNER);
    out.push("");
    out.push("# Official vs Custom OpenAPI Diff");
    out.push("");
    out.push(
        "Machine-generated comparison of Clockify's official OpenAPI surface against this repo's",
        "corrected snapshot. The official source here is the committed offline snapshot",
        `(\`${SPEC_PATHS.official}\`); refresh it with \`make official-openapi-fetch\` to compare against`,
        `the live \`${OFFICIAL_OPENAPI_URL}\`. operationId is unreliable as a join key (only a minority`,
        "match), so operations are joined on HTTP method + path with positional parameters.",
    );
    out.push("");
    out.push("## Summary");
    out.push("");
    out.push(
        table(
            ["Metric", "Count"],
            [
                ["Official operations", String(s.officialOps)],
                ["Custom operations", String(s.correctedOps)],
                ["Shared (matched)", String(s.common)],
                ["New official (missing in custom)", String(s.newOfficial)],
                ["Custom-only (not in official snapshot)", String(s.customBetter)],
                ["Wire-shape conflicts (shared ops)", String(s.conflicts)],
                ["Phantom-risk (custom op, dead live route)", String(s.phantomRisk)],
            ],
        ),
    );
    if (officialSource) {
        out.push("");
        out.push(`Official source: \`${officialSource}\`.`);
    }
    out.push("");
    out.push("## NEW_OFFICIAL_ENDPOINT — in official, missing in custom");
    out.push("");
    out.push(
        diff.newOfficial.length === 0
            ? "_None in the committed snapshot. Run `make official-openapi-fetch` to surface live additions._"
            : table(
                  ["Method", "Path", "operationId"],
                  diff.newOfficial.map((op) => [op.method, `\`${stripV1(op.rawPath)}\``, op.operationId ?? "—"]),
              ),
    );
    out.push("");
    out.push("## CUSTOM_BETTER — present only in the custom spec");
    out.push("");
    out.push("Host/auth overrides:");
    out.push("");
    for (const note of [...diff.hostNotes, ...diff.authNotes]) out.push(`- ${note}`);
    if (diff.hostNotes.length + diff.authNotes.length === 0) out.push("- _none_");
    out.push("");
    out.push("Operations:");
    out.push("");
    out.push(
        diff.customBetter.length === 0
            ? "_none_"
            : table(
                  ["Method", "Path", "Live status"],
                  diff.customBetter.map((op) => [op.method, `\`${op.rawPath}\``, op.liveStatus ?? "—"]),
              ),
    );
    out.push("");
    out.push("## CONFLICT — shared operations with differing wire shape");
    out.push("");
    out.push(
        "Shallow signal only (response-code set, request-body presence). Deep field-level shape",
        "differences are tracked entry-by-entry in [`live-evidence-index.md`](./live-evidence-index.md).",
    );
    out.push("");
    out.push(
        diff.conflicts.length === 0
            ? "_No shallow wire-shape conflicts on the shared surface._"
            : table(
                  ["Operation", "Difference"],
                  diff.conflicts.map((c) => [`\`${c.key}\``, c.reasons.join("; ")]),
              ),
    );
    out.push("");
    out.push("## PHANTOM_RISK — custom op whose live route is dead");
    out.push("");
    out.push(
        diff.phantomRisk.length === 0
            ? "_none_"
            : table(
                  ["Method", "Path", "Note"],
                  diff.phantomRisk.map((op) => [op.method, `\`${op.rawPath}\``, "live evidence: 404 / not bound"]),
              ),
    );
    out.push("");
    return `${out.join("\n").replace(/\n+$/, "")}\n`;
}

const CONFIDENCE_BUCKETS = {
    "live-success": { label: "HIGH", note: "live request succeeded against the sandbox" },
    "probe-documented": { label: "MEDIUM", note: "probe-lab evidence captured; not a green live call" },
    documented: { label: "LOW", note: "documented only; no probe captured" },
};

export function renderSpecConfidence(correctedOps, diff, parsed) {
    const counts = { HIGH: 0, MEDIUM: 0, LOW: 0, OTHER: 0 };
    for (const op of correctedOps.values()) {
        const bucket = CONFIDENCE_BUCKETS[op.liveStatus]?.label ?? "OTHER";
        counts[bucket] += 1;
    }
    const out = [];
    out.push(GENERATOR_BANNER);
    out.push("");
    out.push("# Spec Confidence");
    out.push("");
    out.push(
        "Why this custom spec is trusted, operation by operation. Confidence is derived from the",
        "`x-clockify-live-status` stamp each operation carries in the corrected OpenAPI, cross-checked",
        "against the evidence ledger (`spec/evidence/discrepancies.md`).",
    );
    out.push("");
    out.push("## Confidence distribution");
    out.push("");
    out.push(
        table(
            ["Confidence", "live-status", "Operations", "Meaning"],
            [
                ["HIGH", "live-success", String(counts.HIGH), "live request succeeded against the sacrificial sandbox"],
                ["MEDIUM", "probe-documented", String(counts.MEDIUM), "probe-lab evidence captured; awaiting a green live call"],
                ["LOW", "documented", String(counts.LOW), "documented only; no probe captured yet"],
                ["—", "other/unstamped", String(counts.OTHER), "no live-status stamp"],
            ],
        ),
    );
    out.push("");
    out.push("## How confidence is earned");
    out.push("");
    out.push(
        "- **HIGH** operations have a recorded live response from the sandbox workspace.",
        "- **MEDIUM** operations are backed by probe-lab fixtures and the discrepancy ledger.",
        "- **LOW** operations are documented from upstream sources but not yet probed.",
        "- **Phantom/dead** routes are quarantined and listed under PHANTOM_RISK in",
        "  [`spec-diff-official.md`](./spec-diff-official.md); they are not advertised as tools.",
    );
    out.push("");
    out.push("## Known open wire-shape risks");
    out.push("");
    const open = parsed.entries.filter((e) => e.status === "open");
    if (open.length === 0) {
        out.push("_No entries are currently marked `open` in the evidence ledger._");
    } else {
        for (const entry of open) out.push(`- \`${entry.slug}\` — status: \`${entry.status}\``);
    }
    out.push("");
    out.push(`Phantom/dead routes quarantined: ${diff.phantomRisk.length}.`);
    out.push("");
    return `${out.join("\n").replace(/\n+$/, "")}\n`;
}

export function renderLiveEvidenceIndex(correctedOps, parsed) {
    const proven = liveProvenStatuses(parsed);
    const liveSuccess = [...correctedOps.values()].filter((o) => o.liveStatus === "live-success");
    const out = [];
    out.push(GENERATOR_BANNER);
    out.push("");
    out.push("# Live Evidence Index");
    out.push("");
    out.push(
        "An index of where the custom spec's claims meet real Clockify behavior. Built from the",
        "`x-clockify-live-status` stamps in the corrected OpenAPI and the entries in",
        "`spec/evidence/discrepancies.md` (raw probe payloads stay out of git per the data-handling",
        "policy).",
    );
    out.push("");
    out.push("## Official says X, live API says Y (resolved divergences)");
    out.push("");
    out.push(
        proven.length === 0
            ? "_none recorded_"
            : table(
                  ["Discrepancy", "Status"],
                  proven.map((e) => [`\`${e.slug}\``, `\`${e.status}\``]),
              ),
    );
    out.push("");
    out.push("## Live-probed operations (live-success)");
    out.push("");
    out.push(
        liveSuccess.length === 0
            ? "_none_"
            : table(
                  ["Method", "Path", "operationId"],
                  sortOps(liveSuccess).map((op) => [op.method, `\`${op.rawPath}\``, op.operationId ?? "—"]),
              ),
    );
    out.push("");
    out.push("## Phantom / dead endpoints quarantined");
    out.push("");
    out.push(
        parsed.phantomEndpoints.length === 0
            ? "_none_"
            : table(
                  ["Slug", "Method", "Path"],
                  parsed.phantomEndpoints.map((p) => [`\`${p.slug}\``, p.method, `\`${p.rawPath}\``]),
              ),
    );
    out.push("");
    return `${out.join("\n").replace(/\n+$/, "")}\n`;
}

// Load everything from the committed snapshots and compute the full report.
export function buildOfficialDriftReport({ officialFile = SPEC_PATHS.official } = {}) {
    const officialDoc = loadSpecFile(officialFile);
    const correctedDoc = loadSpecFile(SPEC_PATHS.corrected);
    const discrepanciesText = fs.readFileSync(path.join(ROOT, SPEC_PATHS.discrepancies), "utf8");

    const official = indexOperations(officialDoc);
    const corrected = indexOperations(correctedDoc);
    const officialSchemes = securitySchemeIndex(officialDoc);
    const correctedSchemes = securitySchemeIndex(correctedDoc);
    const parsed = parseDiscrepancies(discrepanciesText);

    const diff = computeDiff({
        official,
        corrected,
        officialSchemes,
        correctedSchemes,
        phantomEndpoints: parsed.phantomEndpoints,
    });

    return { official, corrected, officialSchemes, correctedSchemes, parsed, diff };
}
