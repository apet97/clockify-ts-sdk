#!/usr/bin/env node
// Generate docs/conformance.md — the claim -> proof matrix.
//
//   node scripts/generate-conformance.mjs --write   regenerate docs/conformance.md
//   node scripts/generate-conformance.mjs --check     fail if docs/conformance.md is stale
//
// Two tables are emitted: a curated set of headline behavior claims, each mapped
// to a concrete proof (a make gate, a smoke/test file, or an evidence doc), and an
// exhaustive gate-coverage table derived from docs/contract-inventory.json so the
// matrix grows automatically as gates are added. Every curated proof pointer is
// validated to exist, so a claim can never reference proof that is not there.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const OUTPUT = "docs/conformance.md";
const BANNER = "<!-- Generated by scripts/generate-conformance.mjs. Do not edit by hand; run `make conformance`. -->";

const args = new Set(process.argv.slice(2));

function read(rel) {
    return fs.readFileSync(path.join(root, rel), "utf8");
}
function exists(rel) {
    return fs.existsSync(path.join(root, rel));
}

const makefile = read("Makefile");
const inventory = JSON.parse(read("docs/contract-inventory.json"));

