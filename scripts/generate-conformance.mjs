function validateProof(claim) {
    const problems = [];
    for (const gate of claim.gates ?? []) {
        if (!makefile.includes(`${gate}:`)) problems.push(`make target ${gate} missing`);
    }
    for (const file of claim.files ?? []) {
        if (!exists(file)) problems.push(`evidence file ${file} missing`);
    }
    return problems;
}

function titleFromId(id) {
    return id
        .split("-")
        .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
        .join(" ");
}

function table(headers, rows) {
    const head = `| ${headers.join(" | ")} |`;
    const sep = `| ${headers.map(() => "---").join(" | ")} |`;
    return [head, sep, ...rows.map((r) => `| ${r.join(" | ")} |`)].join("\n");
}

function render() {
    // Validate every curated proof pointer.
    const dangling = [];
    for (const claim of CURATED_CLAIMS) {
        const problems = validateProof(claim);
        if (problems.length > 0) dangling.push(`${claim.claim}: ${problems.join("; ")}`);
    }
    if (dangling.length > 0) {
        console.error("Conformance matrix has dangling proof pointers:");
        for (const d of dangling) console.error(`- ${d}`);
        process.exit(1);
    }

    const out = [];
    out.push(BANNER);
    out.push("");
    out.push("# Conformance matrix");
    out.push("");
    out.push(
        "Vendor-grade means every behavior claim maps to proof a human or agent can run.",
        "This matrix is generated: the curated claims below are validated to reference real",
        "make gates and evidence files, and the gate-coverage table is derived from",
        "`docs/contract-inventory.json` so it stays complete as gates are added.",
    );
    out.push("");
    out.push("## Headline conformance claims");
    out.push("");
    out.push(
        table(
            ["Claim", "Proof"],
            CURATED_CLAIMS.map((c) => [c.claim, c.proof]),
        ),
    );
    out.push("");
    out.push("## Gate coverage (every contract gate)");
    out.push("");
    out.push(
        "Each capability below is proven by the listed make gate. `make contract-gates` runs the",
        "readiness-contract set; `make perfect-fast` and `make perfect-full` own runtime and heavy proof.",
    );
    out.push("");
    const entries = [...inventory.entries]
        .filter((e) => e && e.target && !e.retired)
        .sort((a, b) => a.id.localeCompare(b.id));
    out.push(
        table(
            ["Capability", "Proof gate", "contract-gates"],
            entries.map((e) => [titleFromId(e.id), `\`make ${e.target}\``, e.contractGates ? "yes" : "no"]),
        ),
    );
    out.push("");
    out.push(`Total gates: ${entries.length}.`);
    out.push("");
    return `${out.join("\n").replace(/\n+$/, "")}\n`;
}

const content = render();

if (args.has("--check")) {
    const current = exists(OUTPUT) ? read(OUTPUT) : null;
    if (current !== content) {
        console.error(`${OUTPUT} is stale. Run \`make conformance\` to regenerate.`);
        process.exit(1);
    }
    console.log("conformance matrix up to date");
} else if (args.has("--write")) {
    fs.writeFileSync(path.join(root, OUTPUT), content);
    console.log(`wrote ${OUTPUT} (${CURATED_CLAIMS.length} curated claims)`);
} else {
    console.error("usage: generate-conformance.mjs [--write|--check]");
    process.exit(2);
}
