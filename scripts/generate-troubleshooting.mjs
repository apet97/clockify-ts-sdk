#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const args = new Set(process.argv.slice(2));
const registry = JSON.parse(fs.readFileSync(path.join(root, "docs", "error-codes.json"), "utf8"));
const outputPath = path.join(root, "docs", "troubleshooting.md");

const lines = [];
lines.push("<!-- Generated by scripts/generate-troubleshooting.mjs. Edit docs/error-codes.json, then run `make troubleshooting`. -->");
lines.push("");
lines.push("# Troubleshooting");
lines.push("");
lines.push("Use stable error codes before matching message text. Messages may change; codes, retryable hints, and recovery guidance are the contract.");
lines.push("");
for (const entry of registry.codes) {
    lines.push(`## \`${entry.code}\``);
    lines.push("");
    lines.push(`Meaning: ${entry.meaning}`);
    lines.push("");
    lines.push(`Retryable: ${entry.retry ? "yes" : "no"}`);
    lines.push("");
    lines.push(`Recovery: ${entry.recovery}`);
    lines.push("");
    lines.push(`Surfaces: ${entry.surfaces.join(", ")}`);
    lines.push("");
}
const expected = `${lines.join("\n")}\n`;

if (args.has("--write")) {
    fs.writeFileSync(outputPath, expected);
    console.log("wrote docs/troubleshooting.md");
    process.exit(0);
}

if (args.has("--check")) {
    const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : "";
    if (current !== expected) {
        console.error("Troubleshooting docs drift: run make troubleshooting.");
        process.exit(1);
    }
    console.log("troubleshooting docs are current");
    process.exit(0);
}

process.stdout.write(expected);
