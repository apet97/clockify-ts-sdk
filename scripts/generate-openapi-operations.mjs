#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const args = new Set(process.argv.slice(2));
const specPath = path.join(root, "spec", "corrected", "clockify.corrected.openapi.yaml");
const jsonPath = path.join(root, "docs", "openapi-operations.json");
const mdPath = path.join(root, "docs", "openapi-operations.md");

const ruby = String.raw`
require 'yaml'
require 'json'

doc = YAML.load_file(ARGV.fetch(0))
component_params = (doc['components'] || {})['parameters'] || {}
methods = %w[get post put patch delete head options trace]
operations = []
(doc['paths'] || {}).sort.each do |path, path_item|
  next unless path_item.is_a?(Hash)
  path_item.each do |method, operation|
    next unless methods.include?(method)
    next unless operation.is_a?(Hash)
    parameters = (operation['parameters'] || []).map do |param|
      next unless param.is_a?(Hash)
      if param.key?('$ref')
        ref_name = param['$ref'].to_s.split('/').last
        param = component_params[ref_name] if component_params.key?(ref_name)
      end
      next unless param.is_a?(Hash)
      {
        'name' => param['name'],
        'in' => param['in'],
        'required' => param['required'] == true,
        'schemaType' => param.dig('schema', 'type')
      }
    end.compact
    operations << {
      'method' => method.upcase,
      'path' => path,
      'operationId' => operation['operationId'],
      'tags' => operation['tags'] || [],
      'sdkGroup' => operation['x-fern-sdk-group-name'],
      'sdkMethod' => operation['x-fern-sdk-method-name'],
      'lastPageHeader' => operation['x-clockify-last-page-header'] == true,
      'hasRequestBody' => operation.key?('requestBody'),
      'responseCodes' => (operation['responses'] || {}).keys.sort,
      'parameters' => parameters
    }
  end
end
puts JSON.pretty_generate({
  'schemaVersion' => 1,
  'source' => 'spec/corrected/clockify.corrected.openapi.yaml',
  'operationCount' => operations.length,
  'operations' => operations
})
`;

function buildInventory() {
    const result = spawnSync("ruby", ["-e", ruby, specPath], { encoding: "utf8" });
    if (result.status !== 0) {
        if (result.stderr) process.stderr.write(result.stderr);
        throw new Error("ruby OpenAPI inventory extraction failed");
    }
    return JSON.parse(result.stdout);
}

function jsonFor(value) {
    return `${JSON.stringify(value, null, 2)}\n`;
}

function cell(value) {
    if (Array.isArray(value)) return value.length ? value.map(String).join(", ") : "-";
    if (value === true) return "yes";
    if (value === false || value === undefined || value === null || value === "") return "-";
    return String(value).replaceAll("|", "\\|");
}

function markdownFor(value) {
    const lines = [];
    lines.push("<!-- Generated by scripts/generate-openapi-operations.mjs. Run `make openapi-operations` after refreshing the corrected OpenAPI snapshot. -->");
    lines.push("");
    lines.push("# OpenAPI Operation Inventory");
    lines.push("");
    lines.push(`Source: \`${value.source}\``);
    lines.push("");
    lines.push(`Operation count: **${value.operationCount}**`);
    lines.push("");
    lines.push("| Method | Path | Operation ID | Tags | Fern SDK group | Fern SDK method | Last-Page | Request body | Responses |");
    lines.push("|---|---|---|---|---|---|---|---|---|");
    for (const op of value.operations) {
        lines.push(`| ${op.method} | \`${op.path}\` | \`${cell(op.operationId)}\` | ${cell(op.tags)} | ${cell(op.sdkGroup)} | ${cell(op.sdkMethod)} | ${cell(op.lastPageHeader)} | ${cell(op.hasRequestBody)} | ${cell(op.responseCodes)} |`);
    }
    lines.push("");
    return `${lines.join("\n")}\n`;
}

const inventory = buildInventory();
const expectedJson = jsonFor(inventory);
const expectedMd = markdownFor(inventory);

if (args.has("--write")) {
    fs.writeFileSync(jsonPath, expectedJson);
    fs.writeFileSync(mdPath, expectedMd);
    console.log("wrote docs/openapi-operations.json and docs/openapi-operations.md");
    process.exit(0);
}

if (args.has("--check")) {
    const currentJson = fs.existsSync(jsonPath) ? fs.readFileSync(jsonPath, "utf8") : "";
    const currentMd = fs.existsSync(mdPath) ? fs.readFileSync(mdPath, "utf8") : "";
    const stale = [];
    if (currentJson !== expectedJson) stale.push("docs/openapi-operations.json");
    if (currentMd !== expectedMd) stale.push("docs/openapi-operations.md");
    if (stale.length > 0) {
        console.error(`OpenAPI operation inventory drift: ${stale.join(", ")}. Run make openapi-operations.`);
        process.exit(1);
    }
    console.log("openapi operation inventory is current");
    process.exit(0);
}

process.stdout.write(expectedJson);
