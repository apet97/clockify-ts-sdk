/**
 * Top-level command groups offered for shell completion. Exported so a
 * contract test can assert it stays in sync with the generated
 * `docs/cli-commands.json` surface (every documented group must complete).
 */
export const COMMANDS = [
    "api",
    "status",
    "doctor",
    "start",
    "stop",
    "log",
    "entries",
    "projects",
    "clients",
    "tasks",
    "tags",
    "webhooks",
    "invoices",
    "expenses",
    "timeoff",
    "scheduling",
    "audit-log",
    "reports",
    "shared-reports",
    "users",
    "completion",
    "help",
] as const;

export type CompletionShell = "bash" | "zsh" | "fish";

export function renderCompletion(shell: CompletionShell): string {
    const commands = COMMANDS.join(" ");
    if (shell === "zsh") return renderZsh(commands);
    if (shell === "bash") return renderBash(commands);
    return renderFish();
}

export function parseCompletionShell(value: string | undefined): CompletionShell {
    const shell = (value ?? "zsh").toLowerCase();
    if (shell === "bash" || shell === "zsh" || shell === "fish") return shell;
    throw new Error("completion shell must be one of: bash, zsh, fish");
}

function renderZsh(commands: string): string {
    return `#compdef clk115 clockify115
# Generated by @clockify115/cli.
_clk115() {
  local -a commands
  commands=(${commands})
  _describe 'command' commands
}
compdef _clk115 clk115
compdef _clk115 clockify115
`;
}

function renderBash(commands: string): string {
    return `_clk115_completion() {
  local cur
  cur="\${COMP_WORDS[COMP_CWORD]}"
  COMPREPLY=( $(compgen -W "${commands}" -- "$cur") )
}
complete -F _clk115_completion clk115
complete -F _clk115_completion clockify115
`;
}

function renderFish(): string {
    return COMMANDS.map((command) => `complete -c clk115 -f -a ${command}`).join("\n") +
        "\n" +
        COMMANDS.map((command) => `complete -c clockify115 -f -a ${command}`).join("\n") +
        "\n";
}
